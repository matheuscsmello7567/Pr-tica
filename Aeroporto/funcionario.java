public class funcionario {

    String nome_funcionario;
    String cpf;
    String email;
    String login;
    String senha;

    public String getNome_funcionario(){
        return nome_funcionario;
    }

    public String getCpf(){
        return cpf;
    }

    public String getEmail(){
        return email;
    }

    public String getLogin(){
        return login;
    }

    public String getSenha(){
        return senha;
    }

}